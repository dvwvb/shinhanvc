                    function movelist(page) {
                        const URLSearch = new URLSearchParams(location.search);
                        URLSearch.set('page', String(page - 1));
                        const newParam = URLSearch.toString();
                        let newPath = location.pathname + '?' + newParam;
                        window.open(newPath, '_self');
                    }

                    function pageOperator(type, pageNumbers0) {
                        var URLSearch = new URLSearchParams(location.search);

                        switch (type) {
                            case 'first':
                                var newPage = pageNumbers0 - 1;
                                URLSearch.set('page', String(newPage));
                                break;

                            case 'front':
                                var newPage = parseInt(URLSearch.get('page')) - 1;
                                if (newPage < (pageNumbers0 - 1)) {
                                    newPage = pageNumbers0 - 1;
                                }
                                URLSearch.set('page', String(newPage));
                                break;

                            case 'back':
                                var newPage = parseInt(URLSearch.get('page')) + 1;
                                if (URLSearch.get('page') == null) {
                                    newPage = 1;
                                }
                                if (newPage > (5 - 1)) {
                                    newPage = 5 - 1;
                                }
                                URLSearch.set('page', String(newPage));
                                break;

                            case 'last':
                                var newPage = 5 - 1;
                                URLSearch.set('page', String(newPage));
                                break;
                        }
                        var newParam = URLSearch.toString();
                        var newPath = location.pathname + '?' + newParam;
                        window.open(newPath, '_self');
                    }

