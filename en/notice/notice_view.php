                                    <div style="padding:10px; border:solid 1px #000; text-align: center;">
                                        <h1 style="font-size: 22px; font-weight: 900;">신한금융지주와의 주식교환으로 인한 세금 안내</h1><br>(내국법인用-외국법인의 국내 고정사업장 포함)
                                    </div>

                                    <br><br>

                                    <strong> I. 주식교환에 응하는 경우</strong> <br>

                                    <strong>1. 주식양도차익에 대한 법인세 과세</strong><br><br>

                                    (1) 네오플럭스 주식과 신한금융지주 주식의 교환에 따라 양도차익이 발생할 경우 이를 각 사업연도 소득에 포함하여 법인세를 신고·납부해야 합니다.<br><br>

                                    (2) 양도차익은 양도가액에서 취득가액과 양도비용을 차감하여 산정하며, 총 양도가액은 주식교환으로 주주가 수령하는 신한금융지주 주식수와 신한금융지주 주식의 주식교환일의 한국거래소 종가를 곱한 금액에 단주대금을 합한 금액이 됩니다.<br><br>

                                    (3) 양도차익이 법인세 과세소득에 포함되어 11% ~ 27.5% (지방소득세 포함)의 세율이 적용됩니다.<br><br>

                                    (4) 본건 주식의 교환일이 속하는 사업연도 종료일이 속하는 달의 말일부터 3개월 이내에 본 건에 대한 법인세를 포함하여 각 사업연도 소득에 대한 법인세를 관할세무서에 신고·납부해야 합니다.<br><br>

                                    <strong>2. 주식양도가액에 대한 증권거래세 과세</strong><br><br>

                                    (1) 네오플럭스 주식과 신한금융지주 주식의 교환 시 양도가액의 0.45% 세율로 증권거래세가 과세됩니다. 이 때 주당 양도가액은 수령하는 신한금융지주 주식의 교환가격인 1주당 32,261원과 주식교환으로 주주가 수령한 신한금융지주 주식수를 곱한 금액에 단주대금을 합한 금액이 됩니다.<br><br>

                                    (2) 금융투자업자에 개설된 고객계좌에 주식을 전자등록한 경우에는 해당 금융투자업자가 증권거래세를 신고·납부해야 합니다.<br><br>

                                    (3) 금융투자업자를 통하지 아니하는 경우에는 주주가 양도일이 속하는 반기의 말일부터 2개월 이내(21년 3월 2일)에 주소지 관할세무서에 증권거래세를 직접 신고·납부해야 합니다.<br><br>

                                    <strong>II. 주식매수청구권을 행사하는 경우</strong><br>

                                    <strong>1. 주식양도차익에 대한 법인세 과세</strong><br>

                                    (1) 네오플럭스 주식에 대한 주식매수청구권을 행사하여 양도차익이 발생하는 경우 이를 각 사업연도 소득에 포함하여 법인세를 신고·납부해야 합니다.<br><br>

                                    (2) 양도차익은 양도가액에서 취득가액과 양도비용을 차감하여 산정하며, 주당 양도가액은 주식매수청구권 행사에 따라 네오플럭스로부터 지급받는 네오플럭스 주식 1주당 가액(네오플럭스가 협의를 위하여 제시하는 가격으로서, 자본시장과 금융투자업에 관한 법률 시행령 제176조의7 제3항 제1호에 따라 산정된 가액은 1주당 2,881원)입니다.

                                    <br><br>

                                    (3) 양도차익이 법인세 과세소득에 포함되어 11% ~ 27.5% (지방소득세 포함)의 세율이 적용됩니다.

                                    <br><br>

                                    (4) 본건 주식매수청구권 행사일이 속하는 사업연도 종료일이 속하는 달의 말일부터 3개월 이내에 본 건에 대한 법인세를 포함하여 각 사업연도 소득에 대한 법인세를 관할세무서에 신고·납부해야 합니다.

                                    <br><br>

                                    <strong>2. 주식양도가액에 대한 증권거래세 과세</strong><br><br>

                                    (1) 네오플럭스 주식에 대하여 주식매수청구권을 행사하는 경우에는 양도가액(위 (2) 참조)의 0.45% 세율로 증권거래세가 과세됩니다.

                                    <br><br>

                                    (2) 금융투자업자에 개설된 고객계좌에 주식을 전자등록한 경우에는 해당 금융투자업자가 증권거래세를 신고·납부해야 합니다.

                                    <br><br>

                                    (3) 금융투자업자를 통하지 아니하는 경우에는 주주가 양도일이 속하는 반기의 말일부터 2개월 이내(21년 3월 2일)에 주소지 관할세무서에 증권거래세를 직접 신고·납부해야 합니다.

